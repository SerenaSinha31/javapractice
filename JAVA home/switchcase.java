public class switchcase {
    public static void main(String[] args){
        char ch = 'd';
        switch(ch){
            case'a':
            System.out.println("samosa");
            break;
            case'b':
            System.out.println("samosased");
            break;
            case'c':
            System.out.println("samosa");
            break;
            default:
            System.out.println("samosafvsdbf");
            break;
        }
    }
}
